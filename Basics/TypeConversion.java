package Basics;

public class TypeConversion {
    public static void main(String[] args) {
//        narrowing conversion
        float x = 6.24f;
        int y = (int)x;
        System.out.println(y);

//        upgrading conversion
        int a = 22;
        float b = a;

        float c = 4.45f;
        /*
        Upgrading Conversions
        1. byte -> short, int, long, float, or double
        2. short -> int, long, float, or double
        3. char -> int, long, float, or double
        4. int -> long, float, or double
        5. long -> float, or double
        6. float -> double
        */






    }
}
